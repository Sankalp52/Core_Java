public class Arithmatic {
    public static void main(String[] args) {
        int num1=3,num2=2;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
}
